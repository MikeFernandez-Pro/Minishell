#include "libft.h"

size_t		ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (dstsize <= 0 && (!src || !dst))
		return (ft_strlen(src));
	while (src[j])
		j++;
	if (j + 1 < dstsize)
		ft_memcpy(dst, src, j + 1);
	else if (dstsize != 0)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
		i++;
	}
	return (j);
}
